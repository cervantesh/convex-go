func TestCodegenHelperEdgeCases(t *testing.T) {
	if isSourceFile("schema.txt") {
		t.Fatal("txt file should not be a source file")
	}
	if refKind("unknown") != "" {
		t.Fatal("unknown ref kind should be empty")
	}
	if constructor("unknown") != "" {
		t.Fatal("unknown constructor should be empty")
	}
	if got := exportedVarName("$", "$"); got != "Function" {
		t.Fatalf("unexpected fallback var name: %q", got)
	}
	if got := titleIdentifierPart(""); got != "" {
		t.Fatalf("empty title part should stay empty, got %q", got)
	}
	for _, invalid := range []string{"", "1api", "bad-name"} {
		if isGoIdentifier(invalid) {
			t.Fatalf("%q should not be a valid Go identifier", invalid)
		}
	}
	stripped := stripCommentsAndStrings("const s = \"escaped \\\" export const nope = query({})\";\nexport const yes = query({});")
	if strings.Contains(stripped, "nope") || !strings.Contains(stripped, "export const yes = query") {
		t.Fatalf("unexpected stripped source: %q", stripped)
	}
}

