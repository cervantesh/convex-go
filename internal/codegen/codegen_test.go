func TestGenerateIgnoresCommentsStringsAndNonFunctionFiles(t *testing.T) {
	dir := t.TempDir()
	writeFile(t, filepath.Join(dir, "messages.ts"), `
// export const commented = query({});
const literal = "export const stringy = mutation({})";
const template = `+"`export const templated = action({})`"+`;
/*
export const blockCommented = internalQuery({});
*/
export const list = query({});
`)
	writeFile(t, filepath.Join(dir, "schema.ts"), `export default defineSchema({});`)
	writeFile(t, filepath.Join(dir, "node_modules", "ignored.ts"), `export const dependency = query({});`)
	writeFile(t, filepath.Join(dir, ".git", "ignored.ts"), `export const git = query({});`)

	got, err := Generate(Config{SourceDir: dir, PackageName: "api", ImportPath: "github.com/cervantesh/convex-go"})
	if err != nil {
		t.Fatal(err)
	}
	body := string(got)
	if !strings.Contains(body, "MessagesList") {
		t.Fatalf("expected real export in generated output:\n%s", body)
	}
	for _, blocked := range []string{"Commented", "Stringy", "Templated", "BlockCommented", "Dependency", "Git", "SchemaDefault"} {
		if strings.Contains(body, blocked) {
			t.Fatalf("generated output included ignored symbol %q:\n%s", blocked, body)
		}
	}
}

