func TestGenerateScansConvexSourcesAndEmitsDeterministicRefs(t *testing.T) {
	dir := t.TempDir()
	writeFile(t, filepath.Join(dir, "messages.ts"), `
import { query, mutation } from "./_generated/server";

export const list = query({
  handler: async () => [],
});

export const send = mutation({
  handler: async () => null,
});
`)
	writeFile(t, filepath.Join(dir, "jobs", "daily.ts"), `
import { action } from "../_generated/server";

export const run = action({
  handler: async () => null,
});
`)
	writeFile(t, filepath.Join(dir, "internal", "admin.ts"), `
import { internalMutation, internalQuery } from "../_generated/server";

export const audit = internalQuery({
  handler: async () => null,
});

export const rebuild = internalMutation({
  handler: async () => null,
});
`)
	writeFile(t, filepath.Join(dir, "_generated", "api.d.ts"), `
export const shouldNotAppear = query({});
`)

	got, err := Generate(Config{
		SourceDir:   dir,
		PackageName: "convexapi",
		ImportPath:  "github.com/cervantesh/convex-go",
	})
	if err != nil {
		t.Fatal(err)
	}
	body := string(got)

	for _, want := range []string{
		"// Code generated by convex-go-codegen; DO NOT EDIT.",
		"package convexapi",
		`import convex "github.com/cervantesh/convex-go"`,
		`var InternalAdminAudit = convex.NewQueryReference[map[string]any, any]("internal/admin:audit")`,
		`var InternalAdminRebuild = convex.NewMutationReference[map[string]any, any]("internal/admin:rebuild")`,
		`var JobsDailyRun = convex.NewActionReference[map[string]any, any]("jobs/daily:run")`,
		`var MessagesList = convex.NewQueryReference[map[string]any, []any]("messages:list")`,
		`var MessagesSend = convex.NewMutationReference[map[string]any, any]("messages:send")`,
	} {
		if !strings.Contains(body, want) {
			t.Fatalf("generated output missing %q:\n%s", want, body)
		}
	}
	if strings.Contains(body, "ShouldNotAppear") {
		t.Fatalf("generated output must ignore _generated files:\n%s", body)
	}
	if strings.Index(body, "InternalAdminAudit") > strings.Index(body, "MessagesList") {
		t.Fatalf("generated refs must be sorted deterministically:\n%s", body)
	}
}

func TestGenerateSanitizesIdentifiersAndRejectsDuplicateNames(t *testing.T) {
	dir := t.TempDir()
	writeFile(t, filepath.Join(dir, "admin-tools", "2jobs.ts"), `
import { query } from "../_generated/server";

export const delete = query({
  handler: async () => null,
});
`)
	got, err := Generate(Config{SourceDir: dir, PackageName: "api", ImportPath: "github.com/cervantesh/convex-go"})
	if err != nil {
		t.Fatal(err)
	}
	if !strings.Contains(string(got), `var AdminToolsN2jobsDelete = convex.NewQueryReference[map[string]any, any]("admin-tools/2jobs:delete")`) {
		t.Fatalf("identifier was not sanitized as expected:\n%s", got)
	}

	duplicateDir := t.TempDir()
	writeFile(t, filepath.Join(duplicateDir, "foo-bar.ts"), `export const baz = query({});`)
	writeFile(t, filepath.Join(duplicateDir, "foo_bar.ts"), `export const baz = query({});`)
	if _, err := Generate(Config{SourceDir: duplicateDir, PackageName: "api", ImportPath: "github.com/cervantesh/convex-go"}); err == nil {
		t.Fatal("expected duplicate generated identifier error")
	}
}

