func TestCodegenHardeningScansAndSortsRefsExactly(t *testing.T) {
	dir := t.TempDir()
	writeFile(t, filepath.Join(dir, "zeta.ts"), `
export const zebra = mutation({});
export const alpha = query({});
`)
	writeFile(t, filepath.Join(dir, "alpha.ts"), `export default query({});`)
	writeFile(t, filepath.Join(dir, "alpha", "beta.tsx"), `export const run = action({});`)

	refs, err := scanSourceDir(dir)
	if err != nil {
		t.Fatal(err)
	}
	want := []functionRef{
		{ModulePath: "alpha", Name: "default", Kind: "query", VarName: "AlphaDefault", ResultType: ""},
		{ModulePath: "alpha/beta", Name: "run", Kind: "action", VarName: "AlphaBetaRun", ResultType: ""},
		{ModulePath: "zeta", Name: "alpha", Kind: "query", VarName: "ZetaAlpha", ResultType: ""},
		{ModulePath: "zeta", Name: "zebra", Kind: "mutation", VarName: "ZetaZebra", ResultType: ""},
	}
	if len(refs) != len(want) {
		t.Fatalf("expected %d refs, got %#v", len(want), refs)
	}
	for i := range want {
		if refs[i] != want[i] {
			t.Fatalf("ref %d = %#v, want %#v", i, refs[i], want[i])
		}
	}
}

func TestCodegenHardeningStripCommentsAndStringsEdges(t *testing.T) {
	source := strings.Join([]string{
		`// export const lineComment = query({});`,
		`const escaped = "not real \" export const stringy = mutation({})";`,
		"const templated = `not real\nexport const templateAction = action({})`;",
		`/* export const blockComment = internalQuery({}); */`,
		`export const real = internalAction({});`,
		`const unterminated = "export const nope = query({})`,
	}, "\n")
	stripped := stripCommentsAndStrings(source)
	for _, blocked := range []string{"lineComment", "stringy", "templateAction", "blockComment", "nope"} {
		if strings.Contains(stripped, blocked) {
			t.Fatalf("stripped source leaked %q:\n%s", blocked, stripped)
		}
	}
	if !strings.Contains(stripped, "export const real = internalAction") {
		t.Fatalf("stripped source lost real export:\n%s", stripped)
	}
	if strings.Count(stripped, "\n") != strings.Count(source, "\n") {
		t.Fatalf("stripCommentsAndStrings should preserve newline count: %q", stripped)
	}
	if len(stripped) != len(source) {
		t.Fatalf("stripCommentsAndStrings should preserve byte length: got %d, want %d", len(stripped), len(source))
	}

	trailingLineComment := `export const real = query({}); // export const fake = mutation({});`
	stripped = stripCommentsAndStrings(trailingLineComment)
	if !strings.Contains(stripped, "export const real = query") {
		t.Fatalf("stripped source lost export before trailing comment:\n%s", stripped)
	}
	if strings.Contains(stripped, "fake") {
		t.Fatalf("stripped source leaked trailing EOF line comment:\n%s", stripped)
	}
	if len(stripped) != len(trailingLineComment) {
		t.Fatalf("trailing line comment strip should preserve byte length: got %d, want %d", len(stripped), len(trailingLineComment))
	}

	unterminatedThenReal := "const bad = \"unterminated\nexport const recovered = query({});"
	stripped = stripCommentsAndStrings(unterminatedThenReal)
	if !strings.Contains(stripped, "export const recovered = query") {
		t.Fatalf("stripped source should recover after unterminated string newline:\n%s", stripped)
	}
	if len(stripped) != len(unterminatedThenReal) {
		t.Fatalf("unterminated string strip should preserve byte length: got %d, want %d", len(stripped), len(unterminatedThenReal))
	}

	unterminatedBlock := "/* export const hidden = query({})"
	stripped = stripCommentsAndStrings(unterminatedBlock)
	if strings.Contains(stripped, "hidden") || strings.Contains(stripped, "*/") {
		t.Fatalf("stripped source leaked unterminated block comment content:\n%s", stripped)
	}
	if len(stripped) != len(unterminatedBlock) {
		t.Fatalf("unterminated block strip should preserve byte length: got %d, want %d", len(stripped), len(unterminatedBlock))
	}

	closedBlock := "/* export const hidden = query({}) */"
	stripped = stripCommentsAndStrings(closedBlock)
	if strings.Contains(stripped, "hidden") || strings.Contains(stripped, "*/") {
		t.Fatalf("stripped source leaked closed block comment content:\n%s", stripped)
	}
	if len(stripped) != len(closedBlock) {
		t.Fatalf("closed block strip should preserve byte length: got %d, want %d", len(stripped), len(closedBlock))
	}
}

func TestCodegenHardeningHelperMappings(t *testing.T) {
	for input, want := range map[string]string{
		"query":            "query",
		"internalQuery":    "query",
		"mutation":         "mutation",
		"internalMutation": "mutation",
		"action":           "action",
		"internalAction":   "action",
		"unknown":          "",
	} {
		if got := refKind(input); got != want {
			t.Fatalf("refKind(%q) = %q, want %q", input, got, want)
		}
	}
	for input, want := range map[string]string{
		"query":    "NewQueryReference",
		"mutation": "NewMutationReference",
		"action":   "NewActionReference",
		"unknown":  "",
	} {
		if got := constructor(input); got != want {
			t.Fatalf("constructor(%q) = %q, want %q", input, got, want)
		}
	}
	for input, want := range map[string]string{
		"messages/list":     "MessagesList",
		"admin-tools/2jobs": "AdminToolsN2jobs",
		"$":                 "Function",
		"123":               "N123",
	} {
		if got := exportedVarName(input, ""); got != want {
			t.Fatalf("exportedVarName(%q, \"\") = %q, want %q", input, got, want)
		}
	}
	if titleIdentifierPart("\xff") != "" {
		t.Fatal("invalid utf8 title part should be empty")
	}
}

func writeFile(t *testing.T, path string, body string) {
	t.Helper()
	if err := os.MkdirAll(filepath.Dir(path), 0o755); err != nil {
		t.Fatal(err)
	}
	if err := os.WriteFile(path, []byte(body), 0o644); err != nil {
		t.Fatal(err)
	}
}
